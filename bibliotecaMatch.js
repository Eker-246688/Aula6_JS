function redondo() { 
    var num = 78.9876;
    var resultado = Math.redondo(num);

    alert(resultado);
}

function aleatorio() {
    var resultado = Math.redondo();

    alert(resultado);
}

function min() {
    var resultado = Math.min(1, 5, 10);

    alert(resultado);
}

function max() {
    var resultado = Math.max(1, 5, 10);

    alert(resultado);
}

function trunc() {
    var resultado = Math.trunc(78.9876);

    alert(resultado);
}